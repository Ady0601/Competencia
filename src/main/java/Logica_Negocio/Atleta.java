public class Atleta {
 
    private String nombre;
    private String nacionalidad;
    private int hora;
    private int minutos;

    public Atleta() {
    }

    public Atleta(String nombre, String nacionalidad, int hora, int minutos) {
        this.nombre = nombre;
        this.nacionalidad = nacionalidad;
        this.hora = hora;
        this.minutos = minutos;
    }
   
}
